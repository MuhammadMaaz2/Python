# Data Types Program

# Integer
number_int = 19
print(f"The Data Type of {number_int} is {type(number_int)}")

# Float
number_float = 16.5
print(f"The Data Type of {number_float} is {type(number_float)}")

# String
name_str = "Your Name"
print(f"The Data Type of {name_str} is {type(name_str)}")

# Boolean
boolean_value = True
print(f"The Data Type of {boolean_value} is {type(boolean_value)}")
